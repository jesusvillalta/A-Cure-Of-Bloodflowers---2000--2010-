                    <p class="c3comentario3"><span class="cancionestitulos">Boys Don't Cry (Los  chicos no lloran)</em>  (2:35).-</span> El primer mega-éxito de los Cure a escala mundial. De  ritmo pegadizo y letra taciturna, la canción nos habla de un chico que intenta  recuperar el amor de una chica, intentando hacerse fuerte a él mismo (como le  ha pasado a cualquier hijo de vecino). The Cure tienen esa capacidad de rápida  conexión con la audiencia, y es que Smith juega al juego de la identificación  con el oyente: "Me echaría a tus pies/Pidiendo perdón/Suplicándote/Pero sé que  es demasiado tarde/Y ahora ya no hay nada que pueda hacer&quot;. ¿Acaso alguna vez nadie  se ha sentido de la misma manera? Y, al final, alguien te acaba diciendo la  típica frase: "los chicos no lloran&quot;. Aunque la maestría de Smith es la lectura  irónica, casi sarcástica, que le da en el mismo estribillo de la canción:  "Así que intento reírme de ello/Cubriéndolo  con mentiras/Intento reírme de ello/Escondiendo las lágrimas de mis ojos/Porque  los chicos no lloran&quot;. El autor de este mismo comentario recuerda estar  llorando hace muchos años atrás con un amigo en una discoteca tras un desengaño  amoroso. Touché, señor Smith... </p>
                    <p class="c3cancioncolor1"><span class="cancionestitulos">Plastic Passion (Pasión por el plástico) (2:14).-</span> La cara B de &quot;Boys Don't  Cry&quot; se tituló &quot;Plastic Passion&quot;, una  pieza un tanto &quot;kitsch&quot; pero de agradable escucha. La letra hace  referencia y/o sátira de la cultura pop de masas, cuyo máximo representante fue  Andy Warhol. &quot;La pasión por el plástico es difícil de controlar/La pasión por  el plástico es un escándalo de rebajas/¡Oh! Es la pasión por el plástico&quot;. La  letra sigue relatándonos esa misma contradicción que produce dicha pasión cuando  uno empieza a coquetear con el éxito: &quot;La pasión por el plástico es una  garantía de oro/La pasión por el plástico me está asesinando/¡Oh! Es la pasión  por el plástico&quot;. La música tiene un ritmo sincopado que entra fácilmente por  los oídos, y que se disfruta durante poco más de dos minutos, una duración perfecta  para acompañar a un single como &quot;Boys Don't Cry&quot;.</p>
                    <p class="c3comentario3"><span class="cancionestitulos">10.15 Saturday Night (10.15 s&aacute;bado noche) (3:42).-</span> Ésta es la pieza inaugural de un disco difícil de calificar, a medio camino entre la rebeldía punk y el pop más melodioso. Es un tema puramente descriptivo y minimalista: el punteo constante de la guitarra de Smith simula el goteo del grifo del que habla la canción, al igual que las campanadas, y es precisamente este goteo y ese tic-tac del reloj, marcado por Lol, el que va creando un clima triste y nostálgico al que poco a poco se le unen batería y bajo. El tema describe una atmósfera muy conocida: estamos un sábado por la noche solos en casa sin saber qué hacer, esperando a que suene el teléfono y a que algún amigo/a nos saque de nuestro aburrimiento y de nuestra angustia vital. Este tema, quizá el más recordado del disco, ya explica de forma clara lo que será el motivo central de todo el Lp: la imposibildad de rebelarse a la cruda realidad. &quot;10.15 Saturday Night&quot; inicia ese &quot;leitmotiv&quot; que tienen todas las canciones del disco y, quizá sea el que mejor resuma ese aire depresivo que tiene este primer disco, no en vano se eligió este tema como primer sencillo. Ciertamente, a nivel musical la canción también tiene esa misma estructura cíclica de normalidad - intento de rebeldía - y vuelta a la normalidad, y es que, como autor y letrista de todas las canciones de The Cure hasta la fecha, Robert Smith, gran admirador de personajes como Elvis Costello o Albert Camus, no acostumbra a hacer letras vacías de contenido, y aspira a un cierto nivel intelectual por parte de sus oyentes. </p>
                    <p class="c3cancioncolor3"><span class="cancionestitulos">Accuracy (Precisión) (2:18).-</span> Los intentos por escapar de esa desesperante cotidianidad prosiguen en  la siguiente pieza, &quot;Accuracy&quot; (Precisión), una canción que iba a  llamarse en un principio &quot;Safe Sex&quot;. Este tema sigue con el mismo aire  que el anterior, pesimista, deprimente e indefinida; hay muchas  posturas para actuar contra el mundo, pero lo más fácil es no adoptar  ninguna. Todo es aburrido, no hay nada que hacer. Lo único que hay  seguro es el sexo, y a veces hasta el sexo se acaba conviertiendo en lo  mismo: &quot;Me miras a los ojos/y sonreímos/podría matarte/sin ni siquiera  intentarlo/eso es precisión&quot;. Igualmente, continúan por el fetichismo y  la fascinación por los objetos de la vida cotidiana: &quot;espejito,  espejito en la pared&quot;. Andy Warhol decía que el pop no era una sátira,  sino una manera de apreciar los objetos cotidianos, Robert Smith tan  sólo intenta plasmar toda una filosofía de vida que adoptan los jóvenes  de su época. Smith continua con sus punteos de guitarra mientras  Dempsey describe unas progresiones casi virtuosas, y Tolhurst mantiene  un ritmo lo bastante monótono como para aburrir el tema. Buena pieza,  pero no tanto como la anterior.</p>
<p class="c3cancioncolor3"><span class="cancionestitulos">So What (&iquest;Y qu&eacute;?) (2:35).-</span> Descarada y directa, &quot;So What&quot; es mucho más honesta que la anterior &quot;Grinding Halt&quot; en la que queda mucho más claro el tono de rebeldía que aún arrastraba un agonizante punk en vías de extinción y en el que los Cure se formaron. Junto con la ya citada &quot;Grinding Halt&quot;, &quot;Object&quot; e &quot;It's Not You&quot;, &quot;So What&quot; viene a engrosar el listado de las piezas rebeldes y anarquícas de un disco de naturaleza hetereogénea, y eso tan sólo viene a confirmar lo que muchos críticos y entendidos en The Cure han dicho hasta el momento de su primer trabajo: demasiadas buenas ideas que no acaban de cuajar en un todo unitario. Así pues, pasamos de pistas de la más pura introspección como &quot;10.15 Saturday Night&quot;, &quot;Another Day&quot; o la propia &quot;Three Imaginary Boys&quot; a temas como este &quot;So What&quot;, de griterío e histeria total, en el que los Cure van a tener que hacer mucho más para que algunos los tomen en serio. Y precisamente esto confirma que lo que la banda de Robert Smith intentaba hacer era tan sólo encontrar su propio camino en medio de tantas voces. Comentado más concretamente el caso de la irónica &quot;So What&quot;, cuya letra se inspira en el dorso de un paquete de azúcar, Smith canta (bueno mejor dicho, grita) con toda su rabia y energía, parece como un histérico vendedor de teletienda o un profeta enloquecido sermoneando a su generación (como ya hicieron en su tiempo los The Who con su celebérrima &quot;My Generation&quot;). Más tarde confesó que estaba borracho.</p>
<p class="c3cancioncolor2"><span class="cancionestitulos"><strong><em>Jumping Someone Else's Train</em></strong> (<em>Coger un tren en marcha</em>) (2:56).-</span> Segundo single que se extrajo de este álbum y tercero en la carrera de los Cure, esta danzarina pieza no acabó formando parte de su primer trabajo de estudio "Three Imaginary Boys&quot;, pero sí que fue incorporado luego en su posterior edición americana. La letra de la canción habla de la necesidad de cambio para no quedarse caduco: "No tardarás mucho/En aprender la nueva sonrisa/Te tienes que adaptar/O estarás pasado de moda/Es siempre lo mismo/Coges el tren en marcha&quot;. La línea de bajo que dibuja Dempsey es realmente de un virtuosismo absoluto y resulta de lo mejor de la canción. Tolhurst está igual de acertado a la batería y marca un ritmo frenético y vigoroso, cual si fuera un trayecto en tren que no deja de acelerar. Smith está igual de virtuoso a la guitarra, y todo el tema destila un clara herencia punk de rebeldía, aunque ese virtuosismo en los instrumentos no acabe de emparentarlo propiamente con dicho movimiento musical. La revista Rolling Stone lo llamó como 'rock minimalista' o 'post-punk inventivo', de los que, el trío original The Cure, son sus máximos deudores.</p>

<p class="c3cancioncolor1"><span class="cancionestitulos">Subway Song (Canci&oacute;n subterr&aacute;nea) (1:59).-</span> Un cuento de miedo urbano, eso es lo que Smith nos explica, susurrándonos al oído casi en tono de nana. Esta breve pieza trata sobre la historia del asesinato de una chica en algún oscuro subterráneo de una gran ciudad: &quot;Medianoche en el metro/Ella vuelve a casa/e intenta no correr.../Ecos de pasos/la siguen muy de cerca/Pero no se atreve a darse la vuelta...&quot;, cuya muerte queda bien patente en el estrépito que se puede oír al final del tema (tengo que confesar que se me pusieron los pelos de punta la primera vez que lo oí con mi walkman caminando por un callejón de la ciudad en mitad de la noche...). Se introducen nuevos intrumentos como la harmónica tocada por el propio Smith, Dempsey crea todo el clima del tema con su bajo, Smith puntea suavemente y Tolthrust se limita a seguir el ritmo con los platos de su batería para acabar de crear ese cierto clima que tiene de banda sonora de cine negro. &quot;Subway Song&quot; es un pasaje melódico puesto enmedio de un disco excesivamente ruidoso, por sus raíces punk, en el que se le hecha en falta cierta melodía.</p>
<p class="c3cancioncolor1"><span class="cancionestitulos">Killing an Arab (Matando un árabe) (2:22).-</span> En esa misma línea de 'rock minimalista' y de 'post-punk inventivo' se vertebra "Killing An Arab&quot;, uno de los temas más polémicos en toda la historia del grupo. La canción está inspirada en la novela "El extranjero&quot; de Albert Camus, que cuenta lo aburrida que puede llegar a ser nuestra existencia, y en donde un individuo puede llegar a sentirse un extranjero en su propia tierra. En estos tiempos de inmigración masiva, se puede comprender bastante bien el significado de esta canción y, quizá, hasta su morboso estribillo: "Estoy vivo/Estoy muerto/Soy el extranjero/Matando un árabe&quot;. Acorde con el título, Smith describe unos breves acordes arabascos con su guitarra eléctrica en la introducción musical de la canción y, transcurridos pocos segundos después, entra Dempsey con su habitual línea de bajo virtuosista. El último en incorporarse es Tolhurst a la batería que marca un ritmo rápido y pegadizo. La temática es propiamente herencia del punk, anárquica y provocadora, un eco no muy lejano de aquella "Anarchy in the UK&quot; de los míticos Sex Pistols: "Puedo darme la vuelta e irme/O disparar/Mirando fijamente el cielo/Mirando fijamente el sol/Sea lo que sea que elija/Dará lo mismo&quot;. Asimismo, la letra de la canción sirvió como nombre para dos recopilatorios de The Cure, "Standing on a beach&quot;, y la versión Cd/Vhs del mismo, "Staring at the sea&quot;. "Killing An Arab&quot; fue el primer sencillo en la historia de los The Cure pero, incomprensiblemente, no fue incluída en su álbum debut... Su interpretación, erróneamente xenófoba, ha sido fuente de innumerables controversias hasta llegar al extremo de que algunas radios de la época, llegaran a boicotear su emisión. Pese a ello, "Killing An Arab&quot; figura como un clásico absoluto en la historia de los The Cure y del Rock & Roll en general.    </p>

                    <p class="c3cancioncolor1"><span class="cancionestitulos">Fire In Cairo (Fuego en El Cairo) (3:21).-</span> &quot;Fire In Cairo&quot; es un &quot;divertimento&quot;, un oasis en medio del desierto, una deliciosa y juguetona pieza que, como una oleada de aire fresco, hace respirar un disco cargado de angustia vital y pesimismo. Este impresionante cambio de registro de los Cure, me hace recordar los años mozos de los Beatles, no sé por qué: ¿será por el juego melódico de la guitarra de Smith?, ¿por el estribillo pegadizo con cierto regusto a  cancioncilla de verano (ese incesante &quot;F&quot;-&quot;I&quot;-&quot;R&quot;-&quot;E&quot;-&quot;I&quot;-&quot;N&quot;-&quot;C&quot;-&quot;A&quot;-&quot;I&quot;-&quot;R&quot;-&quot;O&quot; más propio de las cheerleaders animando a su equipo favorito que de un grupo de rock con aires existencialistas)?, ¿o quizá sea por el ritmo popy-popy orientado descaradamente hacia un público quinceañero? Lo cierto es que, aún sin ser la canción más comercial de todo el disco, Smith sabe embadurnarla de manera que esto no lo parezca tanto. &quot;Quemo como el fuego/quemo como el fuego en El Cairo&quot;. Un as de Smith que tenía guardado debajo de la manga.</p>
                    <p class="c3cancioncolor1"> <span class="cancionestitulos">Another Day (Otro dia) (3:42).- </span>Preciosa balada que junto con &quot;10.15 Saturday Night&quot; y &quot;Three Imaginary Boys&quot; apunta los caminos que seguirán los futuros The Cure. Volvemos una vez más al pesimismo y a las letras con un profunda carga existencialista. No en vano, los Cure se ganaron a pulso, y en mayor medida con la publicación de este disco, el alias de 'los paladines del existencialismo de la música rock'. Estamos nuevamente ante una canción cíclica que describe un viaje de ida y vuelta hacia un mismo lugar, todo intento de escapar de esa monotonía resulta inútil: &quot;Hay algo que me retiene/y que me hipnotiza&quot;. La guitarra de Smith es más lacónica que nunca, al igual que el bajo de Dempsey, que suena más grave y triste que en otras ocasiones, y, en cuanto a Tolhurst, parece como si le costase seguir el ritmo. Todo contribuye a crear una atmósfera desasosegada y viciada, en la que casi te cuesta respirar. Un tema ideal para escuchar un día gris de lluvia mirando a través de la ventana. </p>
                    <p class="c3cancioncolor2"><span class="cancionestitulos">Grinding Halt (Parada en seco) (2:50).-</span> Para mí, la canción más floja de todo el disco que destila tufos de un punk rancio y sin mucho sentido. &quot;Grinding Halt&quot; no se aproxima a la consistencia ni a la identidad que posee &quot;So What&quot;, en la que sí que se aprecia un punk mucho más claro y conciso, y es que el problema que tiene &quot;Grinding Halt&quot; es que Smith no se la cree ni él mismo. &quot;No hay luz/no hay gente/no hables/no hay coches/no hay comida...&quot; y así hasta un total de 17 negaciones. En conjunto, resulta una pieza lo bastante resultona como para unos &quot;Backstreet Boys&quot; de estar por casa, pero nunca para una banda de la talla de los Cure. Con un final realmente horroso y una letra sin mayor secreto, este gracioso tema tan sólo sirve para airear un poco la atmósfera angustiosa y depresiva que se respira en la mayoría de las pistas del disco. En resumen, un pop fácil para consumo adolescente que a Smith se le puede perdonar.</p>
                    <p class="c3cancioncolor3"><span class="cancionestitulos">Three Imaginary Boys (Tres chicos imaginarios) (3:13).-</span> Pero, sin duda alguna es esta pieza, juntamente con &quot;10.15 Saturday Night&quot;, otro de los dos temazos del disco. Smith es coherente consigo mismo y acaba el disco con una especie de lamento existencialista, &quot;con un grito de socorro&quot; (Jesús Llorente, &quot;Las vidas de Robert Smith&quot;, Editorial La Máscara, 1997). &quot;Esperando el mañana/que nunca llega/En lo más profundo de mi ser/la sensación de vacío/Con todo lo que la noche me deja/Tres chicos imaginarios&quot;, versos como estos vienen a ser la quintaesencia del disco que encuentra en esta pieza la confirmación de esa angustia vital que ya empieza a asustar a Smith. Tanto la música como la letra de la pieza describen un clima onírico, casi terrorífico que apunta de manera obvia a un &quot;A Forest&quot; o, incluso, a un &quot;Lullaby&quot;. Este trágico epílogo viene a concluir un disco amorfo (sin una forma fija) e irregular, pero, al mismo tiempo, espontáneo, fresco y tremendamente joven y brillante, con grandes dosis de talento incontrolado de una de las que serán las más grandes bandas del rock de todos los tiempos. Lo demás es historia. &quot;Three Imaginary Boys&quot; viene a señalar los senderos que tomaría Smith a partir de ahora, al que se le iba truncar su juventud en poco menos de 17 segundos... 
                        <br>
                  </p>
<p class="c3cancioncolor1"><span class="cancionestitulos">VIDEO - <strong><em>Jumping Someone Else's Train</em></strong> (<em>Coger un tren en marcha</em>) (Dir: Tim Pope, Febrero 1986).- </span>Insultante vídeo que describe la salida de un tren  desde la estación Victoria de Londres hasta llegar a la estación de Brighton.  Las imágenes pasan a cámara rápida, para casar con el ritmo de la canción, y en  él vemos el típico paisaje que se suele ver yendo en tren. La banda brilla por  su ausencia, y ya no hay nada más que comentar (excepto que ví como lo  utilizaron como retroproyección durante su última gira para acompañar este mismo  tema en directo). Ahora sí que ya no hay más a comentar.<br><br>
</p>
<p class="c3cancioncolor1"><span class="cancionestitulos">VIDEO - <strong><em>Boys Don't Cry </em></strong>(<em>Los chicos no lloran</em>) (Dir: Tim Pope, Marzo 1986).- </span>Vídeo que años después vendría a ilustrar una de las canciones más populares de la banda. Smith quiso festejar con este vídeo, la publicación en 1986 de la remezcla de "Boys Don't Cry (New Voice – New mix)&quot; que no fue recibida de manera muy entusiasta entre sus seguidores más acérrimos. Para ello, Tim Pope filmó un vídeo minimalista y nostálgico en el que contaba la colaboración del exbajista Michael Dempsey, a petición del propio Smith. Las sombras de Tolhurst, Smith y Dempsey se proyectan en una pared mientras que a primer término sus roles de músicos son reemplazados por unos niños cantando la melodía en 'playback', y que vienen a suplir a los verdaderos músicos. El vídeo es simple pero resultón, y poco tiempo pasó para que entrara a formar parte de la iconografía propia del grupo. <br><br>
</p>
<p class="c3cancioncolor1"><span class="cancionestitulos">VIDEO - <strong><em>Killing an Arab </em></strong>(<em>Matando un árabe</em>) (Dir: Tim Pope,  1986).- </span>Viendo este vídeo, me hace pensar en "El viejo y el mar&quot; de Ernest Hemingway, y es que Smith no suele escribir letras carentes de contenido. El vídeo es un gran ejemplo de ello: un viejo se pasea por una playa árida y helada. Se fuma un cigarrillo, se cruza con un pescador con quien no media palabra alguna, y luego se queda mirando hacia el horizonte donde ve tres figuras indefinidas. El referente más claro y directo en el que se inspira este vídeo es el movimiento de la "Nouvelle Vague&quot;, con François Truffaut como máximo exponente, el cual adopta la necesidad de retratar la realidad organizando la percepción de un modo sumamente verosímil. Un estilo fílmico muy acertado y acorde con la letra y las imágenes que acompañan a este vídeo.</p>
