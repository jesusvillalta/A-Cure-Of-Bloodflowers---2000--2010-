                <td><p class="c3intro">&quot;J&oacute;venes y rebeldes&quot;</p>
                  <p class="c3intro">Three Imaginary Boys (Fiction, 1979)<br>
  Formaci&oacute;n: R.Smith/L.Tolhurst/M.Dempsey<br>
  Calificaci&oacute;n: *** <br>
                    (para rebeldes trasnochados)</p>
                    <p class="c3intro">&quot;Hab&iacute;a sentido nostalgia de mi propia juventud mientras todav&iacute;a la 
  estaba viviendo.&quot; Robert Smith</p>
                    <p><span class="recomendacionesnegr">Imprescindibles:</span><span class="recomendaciones"> 10.15 Saturday Night, Fire In Cairo, Three Imaginary Boys.<br>
  </span><span class="recomendacionesnegr">Prescindibles:</span><span class="recomendaciones"> Grinding Halt, Foxy Lady, Weedy Burton.</span></p>
                    <p><br>
                    </p>
                    <p class="c3comentario2">En los albores de los años 80 (una década que marcaría tan drásticamente los caminos de la música rock que ya nunca más volvería a ser la misma), un nuevo grupo asomaba tímidamente la cabeza allá por finales de los 70, eran los &quot;Easy Cure&quot; (nombre embrionario de los que siempre hemos conococido como los The Cure). ¿Cuántas biografías de grupos empiezan de la misma manera?: ésta es la historia de cuatro chicos que se conocieron en la escuela primaria y decidieron formar un grupo. Lo cierto es que si Peter O'Toole (voz) -que abandonaría el grupo ya en sus principios- Michael Dempsey (bajo), Laurence 'Lol' Tolhurst (batería) y Robert Smith (guitarra y voz) no hubieran decidido ir más en serio en esto de la música, después de haber formado en el instituto su primer grupillo de amiguetes  llamado &quot;The Obelisk&quot; (en el que ya estaba metido incluso el propio Porl Thomson, que aparecería más activamente luego), seguramente que muchos fans diseminados por todo el mundo no hubiéramos tenido la oportunidad de conocernos. </p>
                    <p class="c3comentario1">Después de que &quot;The Obelisk&quot; evolucionase a &quot;Malice&quot; y, a su vez, &quot;Malice&quot; evolucionase a &quot;Easy Cure&quot; (por sugerencia de Lol), finalmente los Cure se dan a conocer de manera comercial como simplemente &quot;The Cure&quot; (La cura). &quot;Easy Cure&quot; es el grupo que compone &quot;Killing An Arab&quot; y que ficha por Hansa Records primero, para después abandonar a esta filial de Ariola y fichar por la nueva discográfica que por aquel entonces estaba montando Chris Parry, Fiction Records, un cazatalentos de grupos más orientados hacia el punk como &quot;Siouxie &amp; The Banshees&quot;. Los singles de &quot;The Cure&quot; (ya con el nombre cambiado) empiezan a sonar y se venden a porrillo. Las 15.000 copias vendidas de &quot;Killing An Arab&quot; obligan a reeditar otras 15.000 con &quot;10.15 Saturday Night&quot; como pista de bonus. Con esta magnífica carta de presentación, Parry les da luz verde para editar su primer álbum de estudio titulado &quot;Three Imaginary Boys&quot;, un disco producto de su tiempo: extraño, tortuoso y &quot;con muestras de un gran talento mal repartido&quot;. Esos tres chicos imaginarios hacen referencia a la portada del disco, un tanto conceptual, en la que sobre un fondo de color rosa chillón salen representados un frigorífico, un aspirador y una lámpara de salón en lugar de Michael, Lol y Robert, respectivamente (lo cierto es que la portada se editó a desavenencia del propio Smith a quien nunca llegó a gustarle). Augurar ya en su disco de debut que los Cure se convertirían en una de las bandas más influyentes del último cuarto del siglo pasado, hubiera sido tarea imposible incluso para el mismísimo Rappel...</p>
                    <p class="c3comentario3">&quot;Three Imaginary Boys&quot; fue un debut poco espectacular, pero no por ello menos interesante, aunque, personalmente, veo una carta de presentación mucho más personal con su posterior &quot;Seventeen Seconds&quot; (trabajo con el que sí que creo que empiezan a labrarse una carrera los Cure). &quot;Three Imaginary Boys&quot; debe verse como un producto de experimentación, de tanteo, de ver por dónde van a ir los tiros. El álbum debut de los Cure no tiene ninguna unidad temática, así como tampoco tiene ninguna coherencia entre las canciones que lo componen. Más bien, parece una recopilación de viejas melodías tocadas para los amigos más próximos en el pub noctámbulo de turno -lo cierto es que muchas de ellas ya llevaban compuestas algunos lustros...- No por ello dejan de ser buenas, pero quizá se les hecha en falta aquella marca de fábrica que tan bien va a caracterizar a los Cure en un futuro.</p>
                    <p class="c3comentario3">No quisiera disgustar a nadie por mis valoraciones, pero como el lector irá viendo a lo largo de esta ficha, mi misión va a ser doble: por un lado dar una visión  general y circunstancial de cada álbum, así como algunas notas biográficas de la banda, y después, un análisis pormenorizado de cada tema. Así pues, mi primera valoración es la siguiente: &quot;Three Imaginary Boys&quot;, como obra entera, no es ninguna maravilla, aunque la calidad de los temas sea buena. Lo más importante a tener en cuenta es que es un campo de pruebas, donde la experimentación musical es lo primordial y el minimalismo una constante. Más tarde ya vendrán sus grandes obras maestras perfectamente acabadas como un &quot;Pornography&quot;, un &quot;Disintegration&quot; o, sobre todo, un &quot;Wish&quot;. Pero, hablando ya más en serio, desde su formación en el 77, los Cure han ido 'curando' a muchísima gente en sus más de 25 años de vida, y la pregunta es: ¿a cuánta gente llegarán a curar más? Antes de que The Cure compusieran sus temazos por los que serán ampliamente conocidos y recordados como la sugerente &quot;A Forest&quot;, la deslumbrante &quot;Charlotte Sometimes&quot;, la vibrante &quot;The Walk&quot; o la terrorífica &quot;Lullaby&quot;, sus pretensiones eran mucho menores, pero no por eso menos importantes. Hélas aquí:<br>
                      <br>
                    </p>
                    <p class="c3comentario3"><span class="cancionestitulos">10.15 Satuday Night (10.15 s&aacute;bado noche) (3:42).-</span> Ésta es la pieza inaugural de un disco difícil de calificar, a medio camino entre la rebeldía punk y el pop más melodioso. Es un tema puramente descriptivo y minimalista: el punteo constante de la guitarra de Smith simula el goteo del grifo del que habla la canción, al igual que las campanadas, y es precisamente este goteo y ese tic-tac del reloj, marcado por Lol, el que va creando un clima triste y nostálgico al que poco a poco se le unen batería y bajo. El tema describe una atmósfera muy conocida: estamos un sábado por la noche solos en casa sin saber qué hacer, esperando a que suene el teléfono y a que algún amigo/a nos saque de nuestro aburrimiento y de nuestra angustia vital. Este tema, quizá el más recordado del disco, ya explica de forma clara lo que será el motivo central de todo el Lp: la imposibildad de rebelarse a la cruda realidad. &quot;10.15 Saturday Night&quot; inicia ese &quot;leitmotiv&quot; que tienen todas las canciones del disco y, quizá sea el que mejor resuma ese aire depresivo que tiene este primer disco, no en vano se eligió este tema como primer sencillo. Ciertamente, a nivel musical la canción también tiene esa misma estructura cíclica de normalidad - intento de rebeldía - y vuelta a la normalidad, y es que, como autor y letrista de todas las canciones de The Cure hasta la fecha, Robert Smith, gran admirador de personajes como Elvis Costello o Albert Camus, no acostumbra a hacer letras vacías de contenido, y aspira a un cierto nivel intelectual por parte de sus oyentes. </p>
                    <p class="c3cancioncolor1"><span class="cancionestitulos">Accurancy (Precisi&oacute;n) (2:18).-</span> Los intentos por escapar de esa desesperante cotidianidad prosiguen en la siguiente pieza, &quot;Accuracy&quot; (Precisión), una canción que iba a llamarse en un principio &quot;Safe Sex&quot;. Este tema sigue con el mismo aire que el anterior, pesimista, deprimente e indefinida; hay muchas posturas para actuar contra el mundo, pero lo más fácil es no adoptar ninguna. Todo es aburrido, no hay nada que hacer. Lo único que hay seguro es el sexo, y a veces hasta el sexo se acaba conviertiendo en lo mismo: &quot;Me miras a los ojos/y sonreímos/podría matarte/sin ni siquiera intentarlo/eso es precisión&quot;. Igualmente, continúan por el fetichismo y la fascinación por los objetos de la vida cotidiana: &quot;espejito, espejito en la pared&quot;. Andy Warhol decía que el pop no era una sátira, sino una manera de apreciar los objetos cotidianos, Robert Smith tan sólo intenta plasmar toda una filosofía de vida que adoptan los jóvenes de su época. Smith continua con sus punteos de guitarra mientras Dempsey describe unas progresiones casi virtuosas, y Tolhurst mantiene un ritmo lo bastante monótono como para aburrir el tema. Buena pieza, pero no tanto como la anterior.</p>
                    <p class="c3cancioncolor2"><span class="cancionestitulos">Grinding Halt (Parada en seco) (2:50).-</span> Para mí, la canción más floja de todo el disco que destila tufos de un punk rancio y sin mucho sentido. &quot;Grinding Halt&quot; no se aproxima a la consistencia ni a la identidad que posee &quot;So What&quot;, en la que sí que se aprecia un punk mucho más claro y conciso, y es que el problema que tiene &quot;Grinding Halt&quot; es que Smith no se la cree ni él mismo. &quot;No hay luz/no hay gente/no hables/no hay coches/no hay comida...&quot; y así hasta un total de 17 negaciones. En conjunto, resulta una pieza lo bastante resultona como para unos &quot;Backstreet Boys&quot; de estar por casa, pero nunca para una banda de la talla de los Cure. Con un final realmente horroso y una letra sin mayor secreto, este gracioso tema tan sólo sirve para airear un poco la atmósfera angustiosa y depresiva que se respira en la mayoría de las pistas del disco. En resumen, un pop fácil para consumo adolescente que a Smith se le puede perdonar.</p>
                    <p class="c3cancioncolor1"> <span class="cancionestitulos">Another Day (Otro dia) (3:42).- </span>Preciosa balada que junto con &quot;10.15 Saturday Night&quot; y &quot;Three Imaginary Boys&quot; apunta los caminos que seguirán los futuros The Cure. Volvemos una vez más al pesimismo y a las letras con un profunda carga existencialista. No en vano, los Cure se ganaron a pulso, y en mayor medida con la publicación de este disco, el alias de 'los paladines del existencialismo de la música rock'. Estamos nuevamente ante una canción cíclica que describe un viaje de ida y vuelta hacia un mismo lugar, todo intento de escapar de esa monotonía resulta inútil: &quot;Hay algo que me retiene/y que me hipnotiza&quot;. La guitarra de Smith es más lacónica que nunca, al igual que el bajo de Dempsey, que suena más grave y triste que en otras ocasiones, y, en cuanto a Tolhurst, parece como si le costase seguir el ritmo. Todo contribuye a crear una atmósfera desasosegada y viciada, en la que casi te cuesta respirar. Un tema ideal para escuchar un día gris de lluvia mirando a través de la ventana. </p>
                    <p class="c3cancioncolor3"><span class="cancionestitulos">Object (Objeto) (3:00).-</span> Insolente, desvergonzada y hasta grosera, así es Object, una canción que parece haber sido compuesta como venganza contra alguna antigua novia de Smith: &quot;Sabes que me excitas/con esos ojos grandes y tus largas piernas/pero no intentes dirigirme palabra/porque no escucharé tus mentiras/A mis ojos, eres tan solo un objeto&quot;. El desengaño amoroso es la base para construir una pieza rápida, con ritmo seco y vibrante. A Smith se le añade un curioso efecto de eco que parece desmaterializarse, el virtuosismo de Dempsey con una escurridiza linea de bajo se nos pierde a lo largo de la canción y Tolthrust toca la batería seguro de si mismo (quizá más ahora que en ningún otro momento del disco).</p>
                    <p class="c3cancioncolor1"><span class="cancionestitulos">Subway Song (Canci&oacute;n subterr&aacute;nea) (1:59).-</span> Un cuento de miedo urbano, eso es lo que Smith nos explica, susurrándonos al oído casi en tono de nana. Esta breve pieza trata sobre la historia del asesinato de una chica en algún oscuro subterráneo de una gran ciudad: &quot;Medianoche en el metro/Ella vuelve a casa/e intenta no correr.../Ecos de pasos/la siguen muy de cerca/Pero no se atreve a darse la vuelta...&quot;, cuya muerte queda bien patente en el estrépito que se puede oír al final del tema (tengo que confesar que se me pusieron los pelos de punta la primera vez que lo oí con mi walkman caminando por un callejón de la ciudad en mitad de la noche...). Se introducen nuevos intrumentos como la harmónica tocada por el propio Smith, Dempsey crea todo el clima del tema con su bajo, Smith puntea suavemente y Tolthrust se limita a seguir el ritmo con los platos de su batería para acabar de crear ese cierto clima que tiene de banda sonora de cine negro. &quot;Subway Song&quot; es un pasaje melódico puesto enmedio de un disco excesivamente ruidoso, por sus raíces punk, en el que se le hecha en falta cierta melodía.</p>
                    <p class="c3cancioncolor2"><span class="cancionestitulos">Foxy Lady* (Mujer astuta) (*compuesta originalmente por Jimy Hendrix) (2:29).-</span> Innecesaria versión del clásico de Hendrix que empieza como si fuera un ensayo de los músicos y acaba con un prolongado acople de amplificador. Quien canta el tema no es ni más ni menos que el bajista Michael Dempsey, que de esta manera se convierte en el primer cantante interpretando una canción en un disco de The Cure. Lo cierto es que el bajista quería cantar alguna de las canciones de Smith, pero este no se lo permitió, así que Dempsey tuvo que conformarse con versionar al maestro Hendrix. En todo caso, es una pieza totalmente sobrante que lo que hace es desprestigiar a una banda intentado encontrar su propia personalidad (y no lo digo porque tenga algo contra Hendrix, que quede claro). Poca cosa se puede decir más de este tema.</p>
                    <p class="c3cancioncolor1"><span class="cancionestitulos">Meathook (Trozo de carne) (2:18).-</span> Producto de la experimentación de su época, surgió este reggae blanco con aires a lo Bob Marley, y que fácilmente colaria como un tema más en alguno de los primeros álbumes de los &quot;The Police&quot;. &quot;Meathook&quot; (trozo de carne) se asemeja enormemente a la posterior &quot;Fire In Cairo&quot; y entraría, junto con ésta, a formar parte de los temas destinados a amenizar un poco el aire opresivo y existencialista que tiene el disco en general. Fácilmente digerible y muy bien construida, &quot;Meathook&quot; apunta los juegos bucales que tanto divertirán a Smith como en las venideras &quot;Lovecats&quot; o &quot;Close To Me&quot;. De tanto en tanto, se le agradece a Smith flirtear con la ligereza y la banalidad.</p>
                    <p class="c3cancioncolor3"><span class="cancionestitulos">So What (&iquest;Y qu&eacute;?) (2:35).-</span> Descarada y directa, &quot;So What&quot; es mucho más honesta que la anterior &quot;Grinding Halt&quot; en la que queda mucho más claro el tono de rebeldía que aún arrastraba un agonizante punk en vías de extinción y en el que los Cure se formaron. Junto con la ya citada &quot;Grinding Halt&quot;, &quot;Object&quot; e &quot;It's Not You&quot;, &quot;So What&quot; viene a engrosar el listado de las piezas rebeldes y anarquícas de un disco de naturaleza hetereogénea, y eso tan sólo viene a confirmar lo que muchos críticos y entendidos en The Cure han dicho hasta el momento de su primer trabajo: demasiadas buenas ideas que no acaban de cuajar en un todo unitario. Así pues, pasamos de pistas de la más pura introspección como &quot;10.15 Saturday Night&quot;, &quot;Another Day&quot; o la propia &quot;Three Imaginary Boys&quot; a temas como este &quot;So What&quot;, de griterío e histeria total, en el que los Cure van a tener que hacer mucho más para que algunos los tomen en serio. Y precisamente esto confirma que lo que la banda de Robert Smith intentaba hacer era tan sólo encontrar su propio camino en medio de tantas voces. Comentado más concretamente el caso de la irónica &quot;So What&quot;, cuya letra se inspira en el dorso de un paquete de azúcar, Smith canta (bueno mejor dicho, grita) con toda su rabia y energía, parece como un histérico vendedor de teletienda o un profeta enloquecido sermoneando a su generación (como ya hicieron en su tiempo los The Who con su celebérrima &quot;My Generation&quot;). Más tarde confesó que estaba borracho.</p>
                    <p class="c3cancioncolor1"><span class="cancionestitulos">Fire In Cairo (Fuego en El Cairo) (3:21).-</span> &quot;Fire In Cairo&quot; es un &quot;divertimento&quot;, un oasis en medio del desierto, una deliciosa y juguetona pieza que, como una oleada de aire fresco, hace respirar un disco cargado de angustia vital y pesimismo. Este impresionante cambio de registro de los Cure, me hace recordar los años mozos de los Beatles, no sé por qué: ¿será por el juego melódico de la guitarra de Smith?, ¿por el estribillo pegadizo con cierto regusto a  cancioncilla de verano (ese incesante &quot;F&quot;-&quot;I&quot;-&quot;R&quot;-&quot;E&quot;-&quot;I&quot;-&quot;N&quot;-&quot;C&quot;-&quot;A&quot;-&quot;I&quot;-&quot;R&quot;-&quot;O&quot; más propio de las cheerleaders animando a su equipo favorito que de un grupo de rock con aires existencialistas)?, ¿o quizá sea por el ritmo popy-popy orientado descaradamente hacia un público quinceañero? Lo cierto es que, aún sin ser la canción más comercial de todo el disco, Smith sabe embadurnarla de manera que esto no lo parezca tanto. &quot;Quemo como el fuego/quemo como el fuego en El Cairo&quot;. Un as de Smith que tenía guardado debajo de la manga.</p>
                    <p class="c3cancioncolor2"><span class="cancionestitulos">It's Not You (No eres tu) (2:52).-</span> Este tema no viene a decir nada nuevo que no se haya dicho ya con las anteriores &quot;Object&quot; o &quot;So What&quot;. Suficiente cinismo y descaro juvenil están bien para una edad que era la que tenían Smith y sus compañeros en aquellos momentos, pero esa rebeldía pronto se va a ver truncada por un pesimismo que se notará en las posteriores composiciones de Robert Smith, un personaje que gozaría poco de su juventud, ya que estaba llamado a un existencialismo que lo atormentaría lo suficiente como para componer algunas de las más grandes canciones jamás escritas. &quot;It's Not You&quot; supone el abandono a esa edad, un último momento antes de iniciar ese camino en el que ya no habría marcha atrás.</p>
                    <p class="c3cancioncolor3"><span class="cancionestitulos">Three Imaginary Boys (Tres chicos imaginarios) (3:13).-</span> Pero, sin duda alguna es esta pieza, juntamente con &quot;10.15 Saturday Night&quot;, otro de los dos temazos del disco. Smith es coherente consigo mismo y acaba el disco con una especie de lamento existencialista, &quot;con un grito de socorro&quot; (Jesús Llorente, &quot;Las vidas de Robert Smith&quot;, Editorial La Máscara, 1997). &quot;Esperando el mañana/que nunca llega/En lo más profundo de mi ser/la sensación de vacío/Con todo lo que la noche me deja/Tres chicos imaginarios&quot;, versos como estos vienen a ser la quintaesencia del disco que encuentra en esta pieza la confirmación de esa angustia vital que ya empieza a asustar a Smith. Tanto la música como la letra de la pieza describen un clima onírico, casi terrorífico que apunta de manera obvia a un &quot;A Forest&quot; o, incluso, a un &quot;Lullaby&quot;. Este trágico epílogo viene a concluir un disco amorfo (sin una forma fija) e irregular, pero, al mismo tiempo, espontáneo, fresco y tremendamente joven y brillante, con grandes dosis de talento incontrolado de una de las que serán las más grandes bandas del rock de todos los tiempos. Lo demás es historia. &quot;Three Imaginary Boys&quot; viene a señalar los senderos que tomaría Smith a partir de ahora, al que se le iba truncar su juventud en poco menos de 17 segundos... </p>
                  <p class="c3cancioncolor1"><span class="cancionestitulos">Weedy Burton (Debilucho Burton) (instrumental) (1:04).-</span> Nunca mejor dicho: debilucho Rock &amp; Roll que viene a ser un epílogo musical totalmente sobrante que lo único que hace es quitarle potencia y trascendencia al clímax final alcanzado por &quot;Three Imaginary Boys&quot; y, al igual que la horrenda versión del &quot;Foxy Lady&quot;, vuelve a quitarle prestigio a una banda destinada a hacer historia en la Historia del Rock.<br>
                        <br>
                  </p>
<p class="c3cancioncolor1"><span class="cancionestitulos">VIDEO - 10.15 Saturday Night (10:15 sábado noche) (Dir: Piers Bedford, Noviembre 1978).-</span> Vídeo protagonizado por los Cure iniciales: Michael Dempsey, Lol Tolhurst y un jovenzuelo Robert Smith (con un corte de pelo a lo Beatle) y con caras demasiado ingenuas como para creérselo tanto a los demás como a ellos mismos, en donde la banda sale tocando en falso directo. No hay concesiones a la narrativa en este vídeo, en el que tan solo se muestra el goteo de un grifo de vez en cuando (en clara referencia a temática de la canción). En resumen, el debut aséptico de los Cure.<br><br>
