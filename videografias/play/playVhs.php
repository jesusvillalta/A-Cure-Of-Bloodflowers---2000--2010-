          <div align="justify" style="color:#FFFFFF">A 
            mediados de 1991 apareci&oacute;, a trav&eacute;s de Windson International 
            Video lo que fue la cuarta producci&oacute;n en video (oficial) de 
            The Cure. Fue recopilado y producido por The Cure y Chris Parry para 
            Fiction Films, y dirigido, a su vez, por Peter Fowler.  
          <p align="justify"> 
            Con ello, Robert Smith pretend&iacute;a: &quot;proporcionar una veraz 
            idea de lo que puede ser un d&iacute;a de trabajo en la vida del grupo&quot;. 
            As&iacute;, Simon Gallup, Porl Thompson, Boris Williams, Perry Bamonte 
            y el propio Robert accedieron a ser perseguidos por una c&aacute;mara 
            durante siete d&iacute;as (no consecutivos), para mostrarnos algunos 
            minutos de su trabajo, con todo lo que ello conlleva, pero haciendo 
            escasas referencias a su vida privada. El resultado fue condensado 
            en dos horas y media y con el nombre de &quot;Play Out&quot;.</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">Primer d&iacute;a: T&amp;C II Club, 
            Londres, 17 de Enero de 1991:</span> Este primer fragmento 
            comienza con los preparativos del concierto (ensayos, pruebas de sonido, 
            el p&uacute;blico comenzando a entrar en el recinto, ...). Los temas, 
            en directo, rescatados son &quot;Wendy Time&quot;, &quot;The Big Hand&quot;, 
            &quot;Away&quot; (estos tres eran nuevos por entonces), &quot;Let's 
            Go To Bed&quot; y &quot;Strange Day&quot;. Tras ello, y ya en el backstage, 
            con el grupo aparece Chris Parry.</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">Segundo d&iacute;a: Wembley Arena, Londres, 
            con motivo del &quot;The Great British Music Weekend&quot;, 19 de 
            Enero de 1991:</span> De nuevo, m&aacute;s im&aacute;genes 
            de los instantes que preceden a la actuaci&oacute;n en directo. Vemos 
            c&oacute;mo revisan listas de canciones que tocar&iacute;an. Tambi&eacute;n 
            aparece Robert acical&aacute;ndose, todo el grupo posando para los 
            fot&oacute;grafos, bromeando e interviniendo en una peque&ntilde;a 
            entrevista. Despu&eacute;s nos ofrecen cuatro temas del concierto: 
            &quot;Pictures Of You&quot;, &quot;Fascination Street&quot;, &quot;Lullaby&quot; 
            y &quot;A Forest&quot;.</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">Tercer d&iacute;a: The Jonathan Ross 
            Show, 23 de Enero de 1991:</span> Se trata esta vez de una 
            actuaci&oacute;n en directo, en el citado programa de televisi&oacute;n. 
            Interpretan &quot;Harold &amp; Joe&quot; y fragmentos de &quot;Hello, 
            I Love You&quot;, &quot;Just Like Heaven&quot; y &quot;The Walk&quot;.</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">Cuarto d&iacute;a: E-Zee Hire Rehearsel 
            Studio, Londres, 22 de Enero de 1991:</span> Podr&iacute;a 
            tratarse de una demo. Sentados en el suelo y en un ambiente de lo 
            m&aacute;s relajado, ensayan &quot;The Blood&quot; y &quot;The Walk&quot; 
            (esta &uacute;ltima con unos instrumentos poco habituales: bongos, 
            xilof&oacute;n y matasuegras).</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">Quinto d&iacute;a: MTV &quot;Unplugged&quot;, 
            24 de Enero de 1991:</span> Muchos son los grupos que han 
            pasado por este, ya conocido, programa de televisi&oacute;n americana, 
            para hacer su actuaci&oacute;n en plan ac&uacute;stico. La de The 
            Cure no est&aacute; publicada oficialmente como la de algunas otras 
            bandas, pero parte de ella puede encontrarse en el bootleg &quot;Acustic 
            Daze&quot;. Pero antes de aparecer ante las c&aacute;maras tendr&iacute;an 
            que desfilar por la sala de maquillaje y todos, excepto Boris, lo 
            hacen (aunque Perry le manden afeitarse primero). Las bromas contin&uacute;an, 
            como la de Simon al ense&ntilde;ar a la c&aacute;mara la agenda de 
            Chris Parry. Los temas seleccionados son &quot;Just Like Heaven&quot;, 
            &quot;A Letter To Elise&quot;, &quot;If Only Tonight We Could Sleep&quot; 
            y &quot;Boys Don't Cry&quot;.</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">Sexto d&iacute;a: Dominion Theatre, 
            Londres, &quot;The Brit Awards&quot;, 9 de Febrero de 1991:</span>
            Mientras fuera la nieve lo cubre todo, en el teatro The Cure 
            hacen las pruebas de sonido para la actuaci&oacute;n del d&iacute;a 
            siguiente en la entrega de premios.</p>
          <p align="justify"> 
            <span style="color:#FF9900; font-weight:bold">S&eacute;ptimo d&iacute;a: Dominion 
            Theatre, Londres, &quot;The Brit Awards&quot;, 18 de Febrero de 1991:</span> 
            En primer lugar el grupo est&aacute; en una oficina, donde Simon, 
            de nuevo haciendo gala de su buen humor, trae una enorme bola de nieve. 
            Despu&eacute;s el resto se encargar&aacute;n de caracterizarla con 
            gafas, sombrero y tel&eacute;fono. Tras ello, m&aacute;s backstage 
            y por fin, la actuaci&oacute;n en directo tocando &quot;Never Enough&quot;. 
            Por &uacute;ltimo, Roger Daltrey (The Who) ser&aacute; el encargado 
            de entregar el premio a The Cure que comparecen, para ello, al completo.</p>
