        <td valign="top">
            <p align="justify"><br>
              Fue dirigido por Tim Pope, del cual, según Robert, fue la idea, 
              ya que &quot;decía estar cansado de dar una imagen simpática de 
              la banda y propuso hacer la película para construir algo más serio 
              y sobre todo, más agresivo&quot;. Aunque, por otro lado, afirma 
              que la idea original nació porque &quot;cada concierto que hacemos 
              ahora alcanza un punto que parecía inaccesible en el pasado y queríamos 
            plasmarlo para siempre&quot;.</span></font></p>
            <p align="justify">Sea 
              como fuere, el caso es que Pope fue quien se encargó de mostrarnos 
              a The Cure en vivo. Robert: &quot;No creo que fuera un riesgo trabajar 
              con Pap (Apodo cariñoso por el que se conoce a Tim Pope en el &quot;círculo 
              Cure&quot;), sólo porque no fuera un director experimentado en películas. 
              Pudimos haber tenido un director &quot;real&quot; para hacer el 
              film del concierto pero no hubiera tenido el conocimiento suficiente 
            acerca de la banda&quot;.</span></font></p>
            <p align="justify">Pero... 
            comencemos a ver la cinta...</span></font></p>
            <p align="justify">Hay 
              una pequeña introducción previa en la que con &quot;Relax&quot; 
              de fondo (canción del LP &quot;Blue Sunshine&quot; de The Glove), 
              vemos como los cinco Cure (Robert Smith, Simon Gallup, Lol Tolhurst, 
              Porl Thompson y Boris Williams), uniformados con traje gris azulado, 
              aparecen en el escenario (con bromita incluida, al quitarle Simon 
              a Robert la peluca que llevaba puesta y lanzarla al público, para 
            mostrarnos el corte de pelo salvaje que el cantante lucía).</span></font></p>
            <p align="justify">Tras 
              un escueto &quot;Hallo&quot;, más la presentación del tema, comienzan 
              con &quot;Shake Dog Shake&quot; interpretado con cierta frialdad. 
              Le sigue &quot;Piggy In The Mirror&quot; después &quot;Play for 
              Today&quot; con gran algaravía por parte del público francés (incluso 
              las primeras filas se animan y empiezan a bailar pogo), detrás suena 
              &quot;Strange Day&quot; y así se suceden diez canciones más que 
              son: &quot;Primary&quot;, &quot;Kyoto Song&quot;, &quot;Charlotte 
              Sometimes&quot;, &quot;In Between Days&quot;, &quot;The Walk&quot;, 
              &quot;A Night Like This&quot;, &quot;Push&quot;, &quot;One Hundred 
              Years&quot;, &quot;A Forest&quot; y &quot;Sinking&quot;. </span><span
  lang=EN-GB style='color:#CCCCCC;'>Después llegan dos bises, el primero con &quot;Close 
              To Me&quot;, &quot;Let's Go To Bed&quot;, &quot;Six Different Ways&quot;, 
              &quot;Three Imaginary Boys&quot; y &quot;Boys Don't Cry&quot;, mientras 
              que el concierto finaliza con &quot;Faith&quot;, &quot;Give Me It&quot;, 
            &quot;10:15 Saturday Night&quot; y &quot;Killing An Arab&quot;.</span></font></p>
            <p align="justify">Un 
              repertorio, sin duda, de lo más variado del que Pope afirmó: &quot;Lo 
              que es sorprendente es la diversidad del material. Comienza con 
              &quot;Shake Dog Shake&quot; y su sonido metálico, sigue con &quot;A 
              Forest&quot; y &quot;Sinking&quot;, que son muy emocionales, después 
              la faceta cómica de Robert con &quot;Close To Me&quot; y &quot;Let's 
            Go To Bed&quot;... los cambios son magníficos&quot;.</span></font></p>
            <p align="justify">Por 
              otro lado, llama la atención que con unos medios tan simples se 
              haya conseguido un efecto tan particular. &quot;Nunca quise hacer 
              una película de un concierto en directo por falta de control de 
              todos los medios necesarios para que el resultado fuera el positivo&quot; 
            afirma Pope.</span></font></p>
            <p align="justify">Robert:&quot;La 
              película recoge diferentes puntos de vista de un concierto del grupo. 
              La cámara se fija en determinados detalles, mostrando cosas en las 
            que nunca te fijas en una actuación, pero nosotros tocamos en directo&quot;.</span></font></p>
            <p align="justify">Y 
              esto, gracias a la manera en que se utilizan las cámaras, una detrás 
              del escenario, otra dentro,... sirviéndose de algún truco empleado 
              ya en algún video-clip, como el de colocar una cámara en el mastil 
              de una guitarra, y otra que, con su imagen simula volar, mientras 
              interpretan &quot;In Between Days&quot;. Usando acertadamente un 
              tema como &quot;Faith&quot; de fondo para mostrarnos una panorámica 
              general del anfiteatro, coronado con la solemnidad de Augusto. O 
              dándonos la impresión de que todo se desmorona, a ritmo de &quot;Give 
            Me It&quot;, con una verdadera locura de luces, imágenes y sonidos.</span></font></p>
            <p align="justify">Las 
              luces y el humo, por otra parte, también juegan su papel. Del blanco 
              y las sombras del grupo (proyectadas en un telón situado detrás 
              del escenario) que predominan en temas como &quot;One Hundred Years&quot; 
              o &quot;Faith&quot;, a la explosión del color que inunda &quot;A 
              Forest&quot;, las luces van combinándose de manera adecuada al ambiente 
            de cada canción.</span></font></p>
            <p align="justify">Durante 
              prácticamente todo el concierto observamos a unos Cure seguros, 
              aparentemente tranquilos (exceptuando quizás a Simon), y aunque 
              no se muestren precisamente distantes tampoco diría que este concierto 
              lo interpretaron con especial pasión (personalmente, no creo que 
              sea uno de los más emocionantes que The Cure haya hecho). Cada cual 
              del grupo realiza su trabajo, con el buen quehacer de costumbre, 
              Simon al bajo, Boris a la batería, Porl combinando guitarra, teclados 
              y saxo, Lol al teclado (por aquellos días Pope dijo que The Cure 
              no podrían existir sin Lol). Aunque como siempre, el protagonismo 
              personal (que no necesariamente musical) lo encarna Robert, que 
              sin demasiadas palabras, va presentando casi todos los temas. Pope: 
              &quot;La mayoría de los grupos están más muertos que vivos en el 
            escenario, pero con The Cure es diferente&quot;.</span></font></p>
            <p align="justify">Robert: 
              &quot;Se puede decir que es aburrido. La productora dijo que ponía 
              dinero para que saliera algo muy vistoso, como de Michael Jackson. 
              Pero decidimos que eso sería engañoso. Renunciamos a los efectos 
              especiales. Cuatro cámaras filmando lo que pasa en el escenario, 
            para demostrar lo aburridos que podemos llegar a ser&quot;.</span></font></p>
            <p align="justify">Realmente 
              no es para tanto, puesto que a lo largo del concierto, también hay 
              momentos divertidos. Por ejemplo, cuando el propio Pope (con una 
              cámara y un ayudante) se decide a perseguir a Robert por todo el 
              escenario mientra tocan &quot;Close To Me&quot; y &quot;10:15 Saturday 
              Night&quot;. Truco que por cierto, no se trata de ocultar ya que 
            podemos ver a la vez tanto al cámara como a los músicos.</span></font></p>
            <p align="justify">El 
              concierto termina con la subida del grupo al lado de la estatua 
              para saludar al público y a las cámaras, mientras suena un tema 
              de las Chiffons. Robert: &quot;Pienso que el usar el &quot;Sweet 
              Talking Guy&quot; (&quot;Dulce Chico Parlante&quot;) de las Chiffons 
            fue una ironía&quot;.</span></font></p>
            <p align="justify">Pope: 
              &quot;Mi convicción de que la música de The Cure era la adecuada 
              para hacer una película surgió de que justo una película puede captar 
              la profundidad emocional. Me preocupaba transmitir al público el 
              ambiente que crea la música de The Cure, y al final se consiguió 
            con una noche de grabación, cinco cámaras y un sonido perfecto&quot;.</span></font></p>
            <p align="justify" style="text-align:right; color:#6699CC; font-weight:bold">LORELEY<br>
            Fanzine -Other Voices-</p>
