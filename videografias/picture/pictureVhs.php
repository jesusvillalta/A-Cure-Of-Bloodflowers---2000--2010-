          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Picture 
            Show (An Improvable Collection)&quot;</span> 
            es la segunda recopilaci&oacute;n de v&iacute;deos de The Cure. Fue 
            editada en 1991 por Polygram-video. En 77 minutos recoge 10 v&iacute;deos 
            pertenecientes a la etapa 1987-90, es decir, de los &aacute;lbunes 
            &quot;Kiss Me Kiss Me Kiss Me&quot;, &quot;Disintegration&quot; y 
            &quot;Mixed Up&quot;. Todos estos v&iacute;deos fueron dirigidos por 
            Tim Pope y recopilados para la ocasi&oacute;n por Robert Smith y el 
            mismo Pope.</p>
          <p> <span style="color:#FFFFCC; font-weight:bold">&quot;Why 
            Can't I Be You?&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, 
            Laurence Tolhurst, Porl Thompson y Boris Williams.</span>
            En este recopilatorio aparece la versi&oacute;n Extended Remix 12&quot;, 
            pero existe otra corta -single-. Se trata de la primera &quot;coreograf&iacute;a&quot; 
            del grupo, entre flashes de luz y con diferentes disfraces. Las novias 
            les ayudaron a elegirlos y llegaron a ser 25, en dos d&iacute;as de 
            rodaje: oso, vampiro, abeja, colegiala, Jocker -de Batman-, Louis 
            Amstrong entre unos pornogr&aacute;ficos labios rojos, detective, 
            Mr Hyde,...</p>
          <p> 
            Robert: &quot;El grupo entero sent&iacute;a que deber&iacute;amos 
            ser coreografiados bailando, como si fuera la &uacute;ltima cosa que 
            la gente esperase de The Cure. La mujer que nos intentaba ense&ntilde;ar 
            se march&oacute; indignada de lo ineptos que &eacute;ramos. Nunca 
            haremos otro as&iacute;, pero es gracioso saber que es para lo que 
            est&aacute; hecho&quot;.</p>
          <p> &quot;La 
            &uacute;ltima frase de la canci&oacute;n dice 'eres simplemente elegante', 
            una expresi&oacute;n que utilizaba mucho Marilyn Monroe. Por eso le 
            doy la vuelta al traje de oso y se convierte en un elegante traje 
            de cola&quot;.</p>
          <p> &quot;Un 
            fan me dijo: 'desear&iacute;a ser t&uacute;'. En la canci&oacute;n 
            le di la vuelta a aquello. Quer&iacute;a ser un oso polar, pero rehus&eacute; 
            a ponerme la cabeza del disfraz&quot;.</p>
          <p> 
            Pope: &quot;Es el v&iacute;deo que siempre quisimos hacer. The Cure 
            bailando es algo que cre&iacute; nunca ver&iacute;a&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Catch&quot;. 
            Formaci&oacute;n: Robert Smith, Simon Gallup, Laurence Tolhurst, Porl 
            Thompson y Boris Williams.</span> 
            Robert pens&oacute; en &quot;Catch&quot; como en un perfecto single 
            veraniego y ese sentido se reflejaba en el v&iacute;deo. Fue rodado 
            en Francia, en el palacio de Gisele Paul-Tisser que actualmente es 
            un museo de m&uacute;sica, pero no les permitieron filmar en el interior 
            sino &uacute;nicamente en el exterior y en los jardines circundantes, 
            con un mar de fondo precioso.</p>
          <p> 
            Robert: &quot;Es sobre una chica a la que conoc&iacute; y que ten&iacute;a 
            una particular manera de reirse. 'A veces intentaba atraparla, pero 
            ni siquiera logr&eacute; atrapar su nombre' era una licencia po&eacute;tica. 
            Tambi&eacute;n la llam&eacute; 'Atrapar' porque Hansa sol&iacute;a 
            utilizar esa palabra: 'escribes buenas canciones, pero no atrapan'. 
            Nos cost&oacute; dos veces m&aacute;s que cualquier v&iacute;deo de 
            los que hemos hecho, 120.000 libras, pero Lol lo destroz&oacute;. 
            Quer&iacute;amos hacer un v&iacute;deo bonito y ese viejo bastardo 
            se dedic&oacute; a pasear, en vaqueros de minero, por la escalera 
            de espiral y ni siquiera se molest&oacute; en simular que estaba tocando 
            el viol&iacute;n&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Hot 
            Hot Hot!!!&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, Laurence 
            Tolhurst, Porl Thompson, Boris Williams y Roger O'Donell.</span> 
            Como ocurre con &quot;Why Can't I Be You?&quot;, &quot;Hot, Hot, Hot!!!&quot; 
            tambi&eacute;n aparece aqu&iacute; en su versi&oacute;n de 12&quot;, 
            aunque igualmente hay otra correspondiente al single. Observamos otra 
            extravagante semi-coreograf&iacute;a con todos los componentes del 
            grupo uniformados con trajes iguales y gafas de sol -&iexcl;y atenci&oacute;n 
            al rapado de Robert!-. Sirvi&eacute;ndose de un truco aparecen como 
            si fueran enanos y haciendo cabriolas al estilo de la m&uacute;sica 
            disco de los a&ntilde;os 70. Tambi&eacute;n utilizan marionetas a 
            modo de p&uacute;blico. Es un v&iacute;deo filmado en blanco y negro.</p>
          <p> 
            Robert: &quot;Trata sobre tres extra&ntilde;as experiencias sexuales: 
            en el s&oacute;tano de un club, en un ferry y en casa, en mi cama. 
            &quot;Alumbrado, golpeando&quot;, es tan solo una analog&iacute;a. 
            Estuve pensando en un libro para ni&ntilde;os de William Maybe sobre 
            un chico al que le cae un rel&aacute;mpago y retrocede en el tiempo. 
            Es uno de esos tios que aparece en el Libro Guinnes de los records 
            y al que le han caido varios rel&aacute;mpagos encima. Le coment&eacute; 
            a Pope que quer&iacute;amos parecer &quot;un grupo de soul bajitos&quot;. 
            Pero &eacute;l entendi&oacute; 'bajitos' como 'enanos' y 'banda de 
            soul' como 'blanco y negro' &quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Just 
            Like Heaven&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, Laurence 
            Tolhurst, Porl Thompson, Boris Williams y Roger O'Donell.</span> 
            Se trata de una canci&oacute;n basada en un sue&ntilde;o y la acci&oacute;n 
            transcurre al borde de un acantilado, aunque se combinan paisajes 
            naturales y artificiales. Es tambi&eacute;n el v&iacute;deo de debut 
            de Mary -la esposa de Robert-, quien baila durante una 'secuencia 
            del sue&ntilde;o'. El grupo tambi&eacute;n aparece tocando.</p>
          <p> 
            Robert:&quot;Un v&iacute;deo serio. Es mi canci&oacute;n favorita 
            del Lp &quot;Kiss Me Kiss Me Kiss Me&quot;. Recoge dos acontecimientos 
            de la vida real. El principal ocurri&oacute; donde est&aacute;bamos 
            filmando, en Beachy Head, 15 a&ntilde;os antes. Mary baila conmigo 
            en el video porque ella fue la chica que estaba en Beachy Head, as&iacute; 
            que ten&iacute;a que ser ella la que apareciera. En la canci&oacute;n, 
            ella se acerca al acantilado y desaparece -otra licencia po&eacute;tica-&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Lullaby&quot;. 
            Formaci&oacute;n: Robert Smith, Simon Gallup, Porl Thompson, Boris 
            Williams y Roger O'Donell.</span> 
            Se trata del v&iacute;deo m&aacute;s ambicioso, elaborado y caro que 
            han filmado The Cure hasta la fecha y todo ello tuvo su recompensa 
            en el premio que recibi&oacute; al mejor v&iacute;deo de 1989 por 
            la industria discogr&aacute;fica brit&aacute;nica. Narra la pesadilla 
            de ser devorado por una ara&ntilde;a, con esa espantosa imagen como 
            final. Mientras se desarrolla el mal sue&ntilde;o de Robert, por all&iacute; 
            van desfilando unos &quot;m&uacute;sicos&quot; bastante decadentes, 
            disfrazados de militares napole&oacute;nicos andrajosos, con maquillaje 
            blanco y aderezados con telara&ntilde;as, ante un Smith que esta vez 
            encarna los papeles de hombre-ara&ntilde;a y v&iacute;ctima a la vez. 
            T&eacute;cnicamente tambi&eacute;n fue un logro para Tim Pope, pues 
            utiliz&oacute; un procedimiento nunca usado antes en Gran Breta&ntilde;a 
            -para filmar el interior de la boca de Robert-.</p>
          <p> 
            Robert: &quot;Rehus&eacute; a tener encima una ara&ntilde;a de verdad. 
            Hab&iacute;a un tio con un ant&iacute;doto y lo intent&eacute;, pero 
            no pude. Fue uno de esos v&iacute;deos que son un despilfarro. Jim 
            Kerr no lo entendi&oacute;, pero mis sobrinos y yo s&iacute;; antes 
            de tener la aprobaci&oacute;n de la audiencia, prefiero la de mis 
            sobrinos&quot;.</p>
          <p> &quot;Las 
            ara&ntilde;as son una de las fobias a las que no he podido adaptarme. 
            Ara&ntilde;as gordas, con patas largas y finas, que parecen que van 
            a romperse, ma hacen sentir realmente extra&ntilde;o. Cuando era joven 
            estaba del todo aterrorizado con las ara&ntilde;as y me las imaginaba 
            en mi cama. Intent&eacute; liberarme de mi fobia de la manera m&aacute;s 
            corriente, acerc&aacute;ndome a ellas y forz&aacute;ndome a cogerlas. 
            Ahora puedo coger una ara&ntilde;a, incluso de las grandes y peludas. 
            Pero no soporto verlas cerca de mi cara. Tim Pope quer&iacute;a persuadirme 
            para llenar una cama con ara&ntilde;as del tipo devoradoras de p&aacute;jaros, 
            horribles. Por supuesto me negu&eacute;, aunque &eacute;l sigue pensando 
            que hubiera sido maravilloso y hasta entr&oacute; en contacto con 
            el cuidador de ar&aacute;cnidos del Zoo de Londres&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Fascination 
            Street&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, Porl Thompson, 
            Boris Williams y Roger O'Donell.</span> 
            Perteneciente a un single para USA, es un v&iacute;deo que muchos 
            europeos vieron por primera vez en el Picture Show. Fue grabado justo 
            despu&eacute;s de &quot;Lullaby&quot;, en el mismo almac&eacute;n 
            de Londres y nos muestran a unos The Cure tocando en una extra&ntilde;a 
            &quot;calle&quot; con viento, bajo una fria luz de tonos azules. Lo 
            m&aacute;s entretenido, provablemente es la superposici&oacute;n de 
            las im&aacute;genes. Hay una rara versi&oacute;n larga que fue emitida 
            por la MTV.</p>
          <p> 
            Robert: &quot;Espantoso. La idea original era un video de Doctor Who, 
            donde hab&iacute;a una cabina telef&oacute;nica y un reloj, con 'habitaciones 
            dentro de otras habitaciones', hecho muy destartaladamente. Pero fue 
            realizado con un presupuesto muy bajo. Normalmente Elektra y Polydor 
            nos dan dinero, pero este single era s&oacute;lo para USA, as&iacute; 
            que s&oacute;lo Elektra lo hizo. Estaba pensado en la calle Bourbon, 
            en Nueva Orleans, cuando la escrib&iacute;; estaba dispuesto a ir 
            all&iacute;, pero pens&eacute; '&iquest;Qu&eacute; co&ntilde;o creo 
            que voy a encontrar all&iacute;?'. Trata de la incredulidad que supone 
            enga&ntilde;arse con la b&uacute;squeda de un momento perfecto&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Lovesong&quot;. 
            Formaci&oacute;n: Robert Smith, Simon Gallup, Porl Thompson, Boris 
            Williams y Roger O'Donell. </span>La 
            idea original era la de intentar captar el sentimiento de la gira, 
            pero est&aacute; claro que debieron de cambiar de opini&oacute;n. 
            No es precisamente uno de los mejores v&iacute;deos de The Cure, si 
            no m&aacute;s bien lo contrario, situado en un decorado muy lejos 
            de parecer una cueva de verdad, con el grupo tocando con una desgana 
            poco habitual en los &uacute;ltimos v&iacute;deos y un Robert Smith 
            convertido en un verdadero 'pr&iacute;ncipe azul'.</p>
          <p> 
            Robert: &quot;Le di a Mary una cinta con esta canci&oacute;n como 
            regalo de boda. Se fue a la otra habitaci&oacute;n para escucharla 
            y cuando regres&oacute; me llen&oacute; de besos. Es la cosa m&aacute;s 
            franca y honesta que jam&aacute;s he escrito. Es sobre el amor, pero 
            tambi&eacute;n sobre la incapacidad de conocer realmente a alguien. 
            La idea era la de una cueva de hadas donde perderse para siempre, 
            pero apareci&oacute; inc&oacute;moda y arreglada para la ocasi&oacute;n. 
            El plano de apertura es espantoso, ese plano fijo de un gigantesco 
            falo que no ten&iacute;a ninguna apariencia de ser una estalactita&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Pictures 
            Of You&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, Porl Thompson, 
            Boris Williams y Roger O'Donell. </span>Fue 
            rodado en las monta&ntilde;as de Escocia , durante una tormenta de 
            nieve -tormenta que por otro lado les oblig&oacute; a permanecer recluidos 
            en un pub durante 4 horas-. Como casi siempre, predominan los elementos 
            cutres -pero geniales-, tales como palmeras, un sol de cart&oacute;n, 
            el tipo -Bruno- que va disfrazado de oso polar... A pesar del frio, 
            parece como si se lo pasaran de miedo, con guerra de bolas de nieve 
            final entre el grupo y el equipo de filmaci&oacute;n. Existe una versi&oacute;n 
            larga especial para Gran Breta&ntilde;a.</p>
          <p> 
            Robert: &quot;He perdido las fotos que siempre llevaba en la cartera. 
            Me di cuenta de que he estado guardando esas cosas viejas, incluso 
            de antes de que yo naciera, para darle un sentido a aquellas cosas 
            a partir de entonces. Para el video quer&iacute;a un ambiente soleado, 
            aunque haya menciones a la nieve en la letra. La nieve parece arena 
            cuando se ve en blanco y negro, as&iacute; que pens&eacute; vamos 
            a empezar como si estuvi&eacute;ramos al sol y despu&eacute;s apareciera 
            la ventisca&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Never 
            Enough&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, Porl Thompson, 
            Boris Williams y Roger O'Donell. </span>Fue 
            filmado en los estudios Magic Eye de Londres en el transcurso de dos 
            d&iacute;as con sus noches, y se trata de otra peque&ntilde;a joya 
            en la videograf&iacute;a de The Cure. Est&aacute; en parte inspirado 
            en &quot;Freaks&quot;, una pel&iacute;cula de culto de los a&ntilde;os 
            30. De nuevo aparecen los componentes del grupo caracterizados, pero 
            esta vez con disfraces bastante m&aacute;s &quot;retorcidos&quot;: 
            hombre lobo, vampiro, siameses, gorda, hombre de dos cabezas, mujer 
            barbuda, etc. Adem&aacute;s aparece Chris Parry como un grotesco &quot;maestro 
            de ceremonias&quot; al comienzo y al final del v&iacute;deo, mientras 
            Tim Pope encarna a un destartalado &quot;cajero de la fortuna&quot; 
            mec&aacute;nico. Todo ello combin&aacute;ndose con secuencias del 
            grupo tocando en un mini-escenario, y Robert colgando al borde del 
            mar.</p>
          <p> 
            Robert: &quot;Esta canci&oacute;n trata acerca de no sentirse satisfecho 
            de cualquier cosa que se haga. Tambi&eacute;n es sobre la reacci&oacute;n 
            ante el &eacute;xito. Es como estar colgando al borde de un embarcadero, 
            en un espect&aacute;culo de monstruos. Es un video excelente. Ya hab&iacute;a 
            visto a otros grupos actuando en escenarios peque&ntilde;os y me gustaba&quot;.</p>
          <p><span style="color:#FFFFCC; font-weight:bold">&quot;Close 
            To Me&quot;. Formaci&oacute;n: Robert Smith, Simon Gallup, Porl Thompson, 
            Boris Williams y Perry Bamonte.</span> 
            Con el subt&iacute;tulo de &quot;Closer Mix&quot;, se trata del single 
            de 1985, remezclado por Paul Oakenfold en 1990. El v&iacute;deo comienza 
            donde termina el del original &quot;Close To Me&quot;, es decir, con 
            el grupo dentro de un armario cayendo al mar. T&iacute;midamente van 
            saliendo del mismo y recre&aacute;ndose en el &quot;fondo marino&quot;, 
            entre ropas, instrumentos y... risas contenidas, con Bruno esta vez 
            disfrazado de pulpo trompetista y Chris Parry como estrella de mar 
            de lo m&aacute;s gracioso.</p>
          <p> 
            Robert: &quot;Dije a Polydor que no pod&iacute;an sacar el remix como 
            single sin un v&iacute;deo nuevo. El anterior acaba con el armario 
            tirado en el acantilado. Pap hab&iacute;a hecho un v&iacute;deo buen&iacute;simo 
            con 'Tom Tom Club' bajo el mar que me encanta. El sentimiento de 'Close 
            To Me' est&aacute; resumido en 'deseo haberme quedado dormido hoy' 
            -sobre la inutilidad de todo'. A veces, a las 9 de la noche tienes 
            la impresi&oacute;n de no haber hecho nada en todo el d&iacute;a&quot;.</p>
          <p> 
            Para las filmaciones que hay entre v&iacute;deo y v&iacute;deo, eligieron 
            fragmentos entre 12 horas de material videogr&aacute;fico privado 
            y el resultado es muy interesante, pues muestra gran variedad de situaciones.</p>
          <p> 
            Robert: &quot;Fue bastante dif&iacute;cil porque quer&iacute;a que 
            los fans de The Cure nos conocieran aparte de los v&iacute;deos e 
            intent&eacute; darles una idea sobre los diferentes caracteres que 
            hay en el grupo, &iexcl;consegu&iacute; que Porl apareciera hablando!. 
            Era la primera vez que alguien le ha escuchado hablar exceptuando 
            su mam&aacute;&quot;.</p>
          <p> 
            As&iacute;, hay fragmentos de entrevistas y actuaciones en TV, del 
            concierto en el estadio de los Doggers de 1989, del de Crystal Palace. 
            Tambi&eacute;n podemos ver escenas cotidianas de las giras -viajes 
            en autob&uacute;s, backstage, ensayos-; actuaciones haciendo play-back, 
            etc. Los cr&eacute;ditos aparecen mientras suena &quot;The Big Hand&quot; 
            instrumental.</p>
          <p> 
            Pope: &quot;Nuestros v&iacute;deos no est&aacute;n hechos necesariamente 
            para vender un producto&quot;.</p>
          <p> 
            Robert: &quot;Me hace feliz ver el picture Show porque nada ha cambiado 
            mucho en el grupo en los &uacute;ltimos 5 o seis a&ntilde;os y el 
            sentimiento b&aacute;sico a&uacute;n est&aacute; ah&iacute;&quot;.</p>
