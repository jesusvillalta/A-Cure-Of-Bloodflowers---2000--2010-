          indicarnos que pronto tenemos que parar el v&iacute;deo...</p>
        <p align="justify">Por &uacute;ltimo hacer referencia a una serie de 
          filmaciones del grupo que se pueden ver entre v&iacute;deo y v&iacute;deo 
