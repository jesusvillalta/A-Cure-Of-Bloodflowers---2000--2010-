          &quot;relajadito&quot;, si lo comparamos con los anteriores hechos por 
          nuestro ya &quot;viejo amigo&quot; (porque despu&eacute;s de este art&iacute;culo...) 
          Tim Pope. Aparecen los Cure tocando, aqu&iacute; se les ve, serios si, 
          pero no tan inm&oacute;viles, ni tan distantes. No es que est&eacute;n 
          muy &quot;festivos&quot;, pero tampoco nos hace falta &iquest;verdad?. 
          El fondo es un suave tono azulado con unos tenues halos de luz que nos 
          muestran la silueta de un &aacute;rbol... mientras que del techo cuelga 
          una especie de banderitas (&iquest;nunca os hab&iacute;as fijado que 
