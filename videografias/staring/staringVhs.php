          pues tem&iacute;a que el v&iacute;deo incitara a los ni&ntilde;os a 
          meterse en los armarios y a tirarse por los acantilados (?). Robert, 
          ante todo, quer&iacute;a que fuera un v&iacute;deo claustrof&oacute;bico 
          (Y estar&eacute;is de acuerdo conmigo en que lo consigui&oacute;), y 
          as&iacute;, utilizando el estudio m&aacute;s grande de Londres, usaron 
          el decorado m&aacute;s peque&ntilde;o.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;A Night Like 
          This&quot; (Tim Pope, Marzo 1986. Formaci&oacute;n: Smith, Gallup, Tolhurst, 
          Thompson, Williams)</span><font color="#CCCC99">..</font> Y 
