      <td><p align="justify">
          &quot;...Llevo dos meses escribiendo un libro sobre The Cure y preparando 
          un v&iacute;deo de recopilaci&oacute;n de todas las filmaciones del 
          grupo desde que empezamos y de eso ya hace 10 a&ntilde;os. Estamos en 
          un momento muy retrospectivo...&quot; </p>
        <p align="justify">Unos meses despu&eacute;s, en abril de 1986, apareci&oacute; 
          esa recopilaci&oacute;n bajo el nombre de &quot;Staring At The Sea&quot; 
          (paralelamente a la colecci&oacute;n de singles &quot;Standing On A 
          Beach&quot;). Dirigido por Tim Pope, constituye el conjunto de videos 
          de The Cure del 79 al 86. A su vez, podemos enfocar este conjunto en 
          dos bloques. En primer lugar se hayar&iacute;an los videos hasta &quot;The 
          Hanging Garden&quot; y que nos muestran a unos Cure serios y con pose 
          de cierta apat&iacute;a. Y en segundo lugar, se encuentran los dirigidos 
          por Pope (exc&eacute;ntrico personaje que ha trabajado con los Cure 
          desde &quot;Let's Go To Bed&quot; hasta hoy) y que nos ha dado a conocer 
          otras &quot;facetas&quot; del grupo, como son el sentido del humor y 
          la iron&iacute;a, pocas veces mostradas hasta entonces.</p>
        <p align="justify">La cinta comienza con <span style="color:#CCCC99; font-weight:bold">&quot;Killing 
          An Arab&quot; (Tim Pope, 1986)</span>. Un v&iacute;deo un tanto 
          aburrido y en el que ni siquiera aparecen los miembros del grupo. Un 
          viejo, una playa, un mar tranquilo... y en el horizonte... tres figuras 
          &quot;imaginarias&quot;.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;10:15 Saturday 
          Night&quot; (Piers Bedford, Noviembre 1978. Formaci&oacute;n: Smith, 
          Tolhurst, Dempsey)</span><font color="#CCCC99">.</font> Aqu&iacute; 
          ya podemos ver a los primeros Cure, muy j&oacute;venes. Sobre un fondo 
          oscuro vemos tocar a los tres chicos que, por entonces, carec&iacute;an 
          intencionadamente de imagen.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Boys Don't Cry&quot; 
          (Tim Pope, Marzo 1986)</span><font color="#CCCC99">.</font> 
          Este v&iacute;deo fue realizado siete a&ntilde;os despu&eacute;s de 
          que la canci&oacute;n apareciera y para la filmaci&oacute;n Robert invit&oacute; 
          a Michael Dempsey. As&iacute; pues, podemos ver, tras la pantalla, a 
          los tres miembros originales (Smith, Tolhurst, Dempsey) intentando seguir 
          los movimientos de tres ni&ntilde;os de unos 12 a&ntilde;os que, delante, 
          simulan tocar sus instrumentos.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Jumping Someone 
          Else's Train&quot; (Tim Pope, Febrero 1986)</span>. La primera 
          vez que lo vi pens&eacute; que m&aacute;s que un v&iacute;deo parec&iacute;a 
          una tomadura de pelo. Bromas aparte, el v&iacute;deo recoge el trayecto 
          en tren desde la Estaci&oacute;n Victoria de Londres hasta Brighton 
          y en el que podemos ver... pues lo t&iacute;pico, vegetaci&oacute;n, 
          t&uacute;neles y sobre todo... la v&iacute;a del tren. Todo ello a c&aacute;mara 
          r&aacute;pida, muy acorde con el ritmo fren&eacute;tico de la canci&oacute;n.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;A Forest&quot; 
          (Dave Hillier, Marzo 1980. Formaci&oacute;n: Smith, Gallup, Tolhurst, 
          Hartley)</span>. Sobre un fondo oscuro, vemos a los cuatro 
          Cure tocar ante unas c&aacute;maras con las que no parecen &quot;llevarse 
          muy bien&quot;. Seriedad al m&aacute;ximo, apat&iacute;a y aburrimiento. 
          Se dir&iacute;a que Simon est&aacute; de mal humor y que Robert tiene 
          sue&ntilde;o. Aparte, im&aacute;genes distorsionadas de, por supuesto, 
          un bosque.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Play For Today&quot; 
          (Dave Hillier, Marzo 1980)</span>. Otro tanto de lo mismo a 
          no ser que esta vez no hay bosque y que el fondo es blanco. Sobre &eacute;l 
          destacan cuatro figuras, vestidas totalmente de negro, est&aacute;ticas, 
          mostr&aacute;ndonos de nuevo cara &quot;de pocos amigos&quot; y pr&aacute;cticamente 
          ajenas a las c&aacute;maras.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Primary&quot; 
          (Bob Rickerd, Marzo 1981. Formaci&oacute;n: Smith, Gallup, Tolhurst)</span>. 
          Nuevo cambio de director y formaci&oacute;n, lo que supone un peque&ntilde;o 
          paso m&aacute;s en la evoluci&oacute;n de los videos de los Cure. Una 
          vez m&aacute;s aparecen tocando, con dos novedades: que Robert toca 
          el bajo y que por primera vez aparece con maquillaje. Todo ello entremezclado 
          con im&aacute;genes de tres ni&ntilde;as muy coquetas.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Other Voices&quot; 
          (Bob Rickerd, Marzo 1981)</span>. La misma formaci&oacute;n. 
          Filmado en una habitaci&oacute;n de formas poco definidas, de nuevo 
          aparecen los Cure tocando con la misma actitud (aunque se les ve algo 
          m&aacute;s &quot;desenvueltos&quot;). Humo e im&aacute;genes distorsionadas 
          nos transportan a otra dimensi&oacute;n.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Charlotte Sometimes&quot; 
          (Mike Mansfield, Octubre 1981. Formaci&oacute;n: Smith, Gallup, Tolhurst)</span>.Lol: 
          &quot;&iexcl;Es vergonzoso!. Quer&iacute;amos que el v&iacute;deo narrara 
          una historia y fuimos a rodarlo a un viejo y abandonado hospital psiqui&aacute;trico, 
          un extra&ntilde;o lugar donde a&uacute;n quedaban habitaciones amuebladas&quot;. 
          Incluso Robert se llev&oacute; a su casa algunos de los viejos cuadros 
          y esculturas que all&iacute; hab&iacute;a.</p>
        <p align="justify">Robert:&quot;Le dimos a Mike Mansfield el libro &quot;Charlotte 
          Sometimes&quot; para que lo leyera. Quer&iacute;amos que fuera un v&iacute;deo 
          realmente misterioso. Pero cuando lo vimos no sab&iacute;amos si re&iacute;r 
          o llorar&quot;.</p>
        <p align="justify">A pesar de todo sigo pensando que es uno de los mejores 
          v&iacute;deos que han hecho The Cure y, sin duda, el m&aacute;s bello. 
          Charlotte recorre el lugar absorta y ajena a los tres pares de ojos 
          que la observan. Un espejismo de si misma, locura, muerte...</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;The Hanging Garden&quot; 
          (Chris Gabrin, Julio 1982. Formaci&oacute;n: Smith, Gallup, Tolhurst)</span>. 
          Otro de los mejores v&iacute;deos que han hecho. Las im&aacute;genes 
          se funden con la m&uacute;sica de manera magistral. Se puede decir que 
          el esp&iacute;ritu de la canci&oacute;n est&aacute; perfectamente representado. 
          Luna, viento, niebla, piedra y tres figuras enmascaradas.</p>
        <p align="justify">Fue rodado a las 7 de la ma&ntilde;ana y con un fr&iacute;o 
          horrible. Las m&aacute;scaras se las hicieron ellos mismos y usaron 
          el equipo que les hab&iacute;a dado fama de chiflados a los Madness, 
          s&oacute;lo que a los Cure no les sali&oacute; el humor deseado.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;The Walk&quot; 
          (Tim Pope, Junio 1983. Formaci&oacute;n: Smith, Tolhurst)</span>. 
          Se improvis&oacute; mucho y fue rodado sobre fondo negro, algo poco 
          habitual por entonces. Para cada toma Lol y Robert se cambiaban ellos 
          mismos el maquillaje y la vestimenta. Ten&iacute;an una gran caja de 
          juguetes de la que Robert iba cogiendo las camisas (a cu&aacute;l m&aacute;s 
          hortera). Lol insisti&oacute; en ponerse un vestido, mientras que Robert 
          sal&iacute;a luciendo un bonito babero...</p>
        <p align="justify">Este v&iacute;deo es bastante surrealista, lleno 
          de s&iacute;mbolos que &quot;cualquiera se atreve a interpretar&quot;. 
          El mago Lol, el beb&eacute; Robert, y una anciana que va interpretando 
          con las manos la letra de de la canci&oacute;n...</p>
        <p align="justify">Pope: &quot;El v&iacute;deo de &quot;The Walk&quot; 
          fue la mejor cosa que hice en mucho tiempo, pues yo me desarroll&eacute; 
          y ellos se desarrollaron, y empezamos a acercarnos a la verdadera personalidad 
          de Robert. El suger&iacute;a ideas. Es ambicioso, pero de una forma 
          divertida y &eacute;l lo sabe. Sus letras son perfectas para filmar 
          porque cada frase tiene un &eacute;nfasis correspondiente&quot;.</p>
        <p align="justify">Robert: &quot;Me pintaba la cara con los dedos antes 
          de cada escena y me puse el collar de huesos negros que le hab&iacute;a 
          robado a Siouxie o a Severin&quot;.</p>
        <div align="justify">Lol: &quot;La BBC no quiso ponerlo, pues al salir 
          Robert y yo maquillados pensaron que &eacute;ramos gays&quot;.</div>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;The Lovecats&quot; 
          (Tim Pope, Octubre 1983. Formaci&oacute;n: Smith, Tolhurst, Anderson, 
          Thompson, Thornalley)</span>. La canci&oacute;n est&aacute; 
          inspirada en &quot;Los Aristogatos&quot; de Walt Disney, concretamente 
          le llam&oacute; la atenci&oacute;n a Robert la escena en que los gatos 
          hacen una fiesta sobre los tejados de Par&iacute;s. El v&iacute;deo 
          fue rodado una noche y es uno de los m&aacute;s divertidos que han hecho.</p>
        <p align="justify">Robert: &quot;Fue una buena noche. Le dijimos a un 
          corredor de fincas que ten&iacute;amos pensado comprar aquella casa 
          en Hamstead, as&iacute; que nos dej&oacute; las llaves. Llevamos todo 
          el equipo de filmaci&oacute;n y a la ma&ntilde;ana siguiente, tras rodar 
          el v&iacute;deo, devolvimos las llaves&quot;.</p>
        <p align="justify">Invitaron a mucha gente y organizaron una fiesta 
          all&iacute;, de manera que alrededor de las 4 de la madrugada, cuando 
          iban a filmar el v&iacute;deo, todos estaban fuera de si. Hab&iacute;an 
          conseguido reunir 30 gatos, pero a pesar de colocar comida en lugares 
          estrat&eacute;gicos, en cuanto se encend&iacute;an los focos, sal&iacute;an 
          disparados.</p>
        <p align="justify">Lol: &quot;Aterroric&eacute;, a eso de las 6 de la 
          ma&ntilde;ana, a un rastafar que caminaba por la calle, al salir yo 
          aparentemente de ninguna parte y disfrazado de gato&quot;.</p>
        <p align="justify">Pope: &quot;No es mi v&iacute;deo favorito porque 
          pienso que es demasiado f&aacute;cil que te guste. Quiz&aacute;s fui 
          un poco lejos, pero de todas formas la canci&oacute;n es como un juego. 
          La imagen de Robert -algo delirante- le preocup&oacute; un poco, pero 
          a mi me gusta la forma en que su personalidad se desarrolla del &quot;Let's 
          Go To Bed&quot; pasando por &quot;The Walk&quot; al &quot;The Lovecats&quot;. 
          Cada vez se iba mostrando m&aacute;s y m&aacute;s extrovertido. Mi forma 
          de trabajar est&aacute; muy pr&oacute;xima a la de Robert en el estudio. 
          El conoce el sentimiento de la canci&oacute;n, conoce la estructura, 
          pero no sabe todos los detalles de antemano&quot;.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;The Canterpillar&quot; 
          (Tim Pope, Julio 1984. Formaci&oacute;n: Smith, Tolhurst, Anderson, 
          Thompson, Thornalley)</span>. Despu&eacute;s de las locas im&aacute;genes 
          que acompa&ntilde;aron a los tres singles anteriores, The Canterpillar, 
          sin embargo, cont&oacute; con un v&iacute;deo m&aacute;s &quot;moderado&quot;. 
          Rodado en un precioso lugar de arquitecturas modernistas y lleno de 
          plantas (supongo que ser&iacute;a un invernadero), Robert esta vez adopta 
          una actitud mucho m&aacute;s modosita, mientras que el resto del grupo 
          no para de decontrolar. A&uacute;n as&iacute;, para Pope &quot;lo &uacute;nico 
          gracioso era ver a Robert con aquella enorme camisa blanca&quot;.</p>
        <p align="justify">&quot;Quer&iacute;amos utilizar mariposas, pero a&uacute;n 
          no las hab&iacute;a en aquella &eacute;poca del a&ntilde;o, por lo que 
          nos las tuvimos que arreglar con orugas&quot;.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;In Between Days&quot; 
          (Tim Pope, Julio 1985. Formaci&oacute;n: Smith, Gallup, Tolhurst, Thompson, 
          Williams)</span>.. Una vez m&aacute;s, el v&iacute;deo surgi&oacute; 
          entre el grupo y Pope. Es el favorito de Robert, el cual quer&iacute;a 
          algo al estilo de los trabajos viejos de los 60 de Dave Clark Five, 
          en blanco y negro. Utilizaron esos alambres que se suelen usar en los 
          circos, para sujetar la c&aacute;mara y balancearla hacia Robert. Uno 
          del equipo sujetaba el cabo para tener la tirantez correcta, pero en 
          la primera toma todo sali&oacute; mal. Calcularon el peso de la c&aacute;mara, 
          pero se olvidaron de contar una pieza, as&iacute; que cuando la lanzaron 
          fue violentamente hacia Robert, el que afortunadamente se dio cuenta 
          de que algo no iba bien y se agach&oacute;, pasando la c&aacute;mara 
          roz&aacute;ndole la cabeza, para despu&eacute;s girar alrededor suyo.</p>
        <p align="justify">La idea de los colores fosforescentes se le ocurri&oacute; 
          a Robert, pero parece ser que Pope lo interpret&oacute; a su manera. 
          Robert le dijo a Pope que le gustar&iacute;a tener flases de colores 
          pasando alrededor de su cabeza. &quot;Colores como los de mis calcetines&quot; 
          insinu&oacute; Pope, a lo que Robert respondi&oacute; afirmativamente. 
          A Pope le pareci&oacute; estupendo pues los calcetines eran baratos 
          y f&aacute;ciles de conseguir, pero... &iexcl;8000 libras se gastaron 
          dibuj&aacute;ndolos y colore&aacute;ndolos para el v&iacute;deo!. &quot;&iexcl;Pu&ntilde;etero 
          mental!, &iexcl;Es monstruoso!&quot; fue la reacci&oacute;n de Robert.</p>
        <p align="justify">Robert: &quot;La grabaci&oacute;n fue agotadora porque 
          tuve que llevar la c&aacute;mara atada con una correa durante 5 horas. 
          Adem&aacute;s nos tuvimos que pintar la cara con colores ultravioleta 
          y permanecer 4 horas con los ojos cerrados, parec&iacute;amos mimos 
          y est&aacute;bamos rid&iacute;culos&quot;.</p>
        <p align="justify"><span style="color:#CCCC99; font-weight:bold">&quot;Close To Me&quot; 
          (Tim Pope, Septiembre 1985. Formaci&oacute;n: Smith, Gallup, Tolhurst, 
          Thompson, Williams)</span>.. Robert le coment&oacute; a Pope 
          que el v&iacute;deo deber&iacute;a captar de alguna manera la clase 
          de claustrofobia que se siente cuando est&aacute;s en un armario del 
